    /**
     * Carga los emails en la tabla. Muestra el remitente y el asunto del
     * mensaje
     *
     * @param emails
     */
    private void cargarEmails(ArrayList<Email> emails) {
        for (Email e : emails) {
            /* for(tipo elemento:conjunto) este for se llama for each*/
            Vector v = new Vector();

            v.add(e);//va a poner en la primera columna un objeto mostrando sólo el remitente
            v.add(e.getAsunto());// va en la segunda columna
            modeloTabla.addRow(v);//añadir una fila
        }
    }

