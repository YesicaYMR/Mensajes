    private void tblEmailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmailsMouseClicked
        Email seleccionado;//creo un objeto tipo email
        int fila = tblEmails.rowAtPoint(evt.getPoint());
        // int columna = tabla.columnAtPoint(e.getPoint()); no me interesa la columna ha pulsado
        if ((fila > -1) /*&& (columna > -1)*/) {
            seleccionado = (Email) modeloTabla.getValueAt(fila, 0);
            lblDe.setText(seleccionado.getDe());
            lblAsunto.setText(seleccionado.getAsunto());
            lblFecha.setText(seleccionado.getFecha());
            txtMensaje.setText(seleccionado.getMensaje());
            chckLeido.setSelected(true);
            seleccionado.setLeido(true);

        }

    }//GEN-LAST:event_tblEmailsMouseClicked

