    private void cmbUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbUsuariosActionPerformed
        int pos = cmbUsuarios.getSelectedIndex();//dame el seleccionado
        if (pos != 0) {
            Usuario u = (Usuario) modeloUsuarios.getElementAt(pos);//dame el seleccionado
            lblNombre.setText(u.getNombre());
            lblEmail.setText(u.getEmail());
            if (u.getPerfil() == 1) {
                chckAdmin.setSelected(true);
                chckEstandar.setSelected(false);
            } else {
                chckEstandar.setSelected(true);
                chckAdmin.setSelected(false);
            }
            btnAceptar.setEnabled(true);
        }
    }//GEN-LAST:event_cmbUsuariosActionPerformed


